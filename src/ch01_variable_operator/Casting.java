package ch01_variable_operator;

public class Casting {
    public static void main(String[] args) {
        double d = 100;
        System.out.println("d : " + d);

        int i = (int)12.5;
        System.out.println("i : " + i);

        System.out.println(14/5);

        System.out.println((double)14/5);

        System.out.println((double)14/5);
    }
}
